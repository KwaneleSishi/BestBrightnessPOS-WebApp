using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using FinalBestBrightnessStore;
using FinalBestBrightnessStore.Models;

namespace FinalBestBrightnessStore.Controllers
{
    public class AdminsController : Controller
    {
        private readonly ApplicationDbContext _context;

        public AdminsController(ApplicationDbContext context)
        {
            _context = context;
        }
        
        public async Task<IActionResult> AdminDashboard()
        {
            var productCount = _context.Products.Count();
            var salesPersonCount = _context.SalesPersons.Count();
            var stockManagerCount = _context.StockManangers.Count();

            // Assume you have a Finance table with a TotalIncome column
            //var totalIncome = _context.Finances.Sum(f => f.amount);
            var totalIncome = (decimal)_context.Finances.Sum(f => f.amount);
            totalIncome = Math.Round(totalIncome, 2);

            var viewModel = new AdminDashboard
            {
                ProductCount = productCount,
                SalesPersonCount = salesPersonCount,
                StockManangerCount = stockManagerCount,
                TotalIncome = totalIncome,
            };

            return View(viewModel);
        }

